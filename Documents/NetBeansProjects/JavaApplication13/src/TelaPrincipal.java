    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
    int linhaSelecionada = tabelaConsultas.getSelectedRow();

    if (linhaSelecionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Selecione uma consulta para excluir."
        );
        return;
    }

    // Remove da lista
    lista.remove(linhaSelecionada);

    // Atualiza a tabela
    atualizarTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnFinaizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinaizarActionPerformed
          int linhaSelecionada = tabelaConsultas.getSelectedRow();

    if (linhaSelecionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Selecione uma consulta para finalizar."
        );
        return;
    }

    Consulta consulta = lista.get(linhaSelecionada);

    TelaFinalizar tela = new TelaFinalizar(this, consulta);
    tela.setVisible(true);
    this.setVisible(false);
    }//GEN-LAST:event_btnFinaizarActionPerformed

