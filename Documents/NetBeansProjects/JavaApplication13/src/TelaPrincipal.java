void atualizarTabela() {
    DefaultTableModel model = (DefaultTableModel) tabelaConsultas.getModel();
    model.setRowCount(0);

    for (Consulta c : lista) {
        model.addRow(new Object[]{
            c.getNome(),
            c.getCpf(),
            c.getTelefone(),
            c.getData(),
            c.isJaEraPaciente() ? "Sim" : "Não",
            c.isRealizada() ? "Sim" : "Não"
        });
    }
}






