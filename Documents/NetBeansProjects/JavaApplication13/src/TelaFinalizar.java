   private void carregarDados() {
    // Checkbox sempre desativada
    jCheckBox1.setEnabled(false);

    // Marca se j√° foi realizada
    jCheckBox1.setSelected(consulta.isRealizada());

    // Se j√° foi realizada, n√£o pode finalizar de novo
    if (consulta.isRealizada()) {
        btnFinalizar2.setVisible(false);
        jTextField1.setText(consulta.getObservacoes());
        jTextField1.setEditable(false);
    }
}

